package com.me.domain;


public class VOPerson
{

    private String ID;
    private int age;
    private String user_name;


    public String getID()
    {
        return ID;
    }


    public void setID(String iD)
    {
        ID = iD;
    }


    public int getAge()
    {
        return age;
    }


    public void setAge(int age)
    {
        this.age = age;
    }


    public String getUser_name()
    {
        return user_name;
    }


    public void setUser_name(String userName)
    {
        user_name = userName;
    }

}
